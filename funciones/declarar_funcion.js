
// declarar una función en JavaScript
// en JavaScript, las funciones se pueden declarar utilizando la palabra clave "function"
// una función es un bloque de código que se puede reutilizar, y que puede recibir argumentos y devolver un valor

function saludar() {
    console.log("Hola, soy una función");
}

saludar(); // se llama a la función para que se ejecute 

function sumar(a, b) {
    return a + b; // la función devuelve la suma de a y b
}

let resultado = sumar(5, 3); // se llama a la función con los argumentos 5 y 3, y se guarda el resultado en la variable "resultado"
console.log(resultado); // se imprime el resultado de la suma, que es 8 
