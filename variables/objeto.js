// en JavaScript, los objetos se representan con el tipo de dato "object"
// un objeto es una colección de propiedades, que pueden ser de cualquier tipo de dato
// las propiedades se representan con un par clave-valor

let profesor = {
    nombre: "Alvaro",
    apellido: "Maurelia",
    edad: 30,
    asignatura: "Programación"
}

// imprimir el objeto completo
console.log(profesor);

// acceder a una propiedad del objeto
console.log(profesor.nombre);
console.log(profesor.apellido);
console.log(profesor.edad);
console.log(profesor.asignatura);