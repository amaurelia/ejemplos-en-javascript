// Declaración de variables en JavaScript
// en JavaScript, las variables se pueden declarar utilizando var, let o const
// var es la forma tradicional de declarar variables, pero tiene un alcance global o de función
// let es la forma moderna de declarar variables, tiene un alcance de bloque
// const se utiliza para declarar constantes, que son variables cuyo valor no puede cambiar

let nombre = "Seba";
console.log(nombre);

let apellido = "Gomez";
console.log(apellido);

var edad = 30;
console.log(edad);

const PI = 3.1416;
console.log(PI);