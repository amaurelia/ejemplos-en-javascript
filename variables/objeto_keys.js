// obtener las claves de un objeto
// en JavaScript, se pueden obtener las claves de un objeto utilizando el método "Object.keys()"
// el método "Object.keys()" devuelve un arreglo con las claves del objeto  

persona = {
    nombre: "Alvaro",
    apellido: "Maurelia",
    edad: 30,
    asignatura: "Programación"
}
// obtener las claves de un objeto utilizando Object.keys()
console.log(Object.keys(persona));