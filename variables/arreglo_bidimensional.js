// arreglo bidimensional
// un arreglo bidimensional es un arreglo que contiene otros arreglos como elementos
// en JavaScript, se puede representar un arreglo bidimensional utilizando un arreglo de arreglos

fila_1 = ["A1", "B1", "C1"];
fila_2 = ["A2", "B2", "C2"];
fila_3 = ["A3", "B3", "C3"];
arreglo_bidimensional = [fila_1, fila_2, fila_3];
console.log(arreglo_bidimensional);

//| acceder a un elemento del arreglo bidimensional
console.log(arreglo_bidimensional[0][0]); // A1
console.log(arreglo_bidimensional[0][1]); // B1
console.log(arreglo_bidimensional[0][2]); // C1
console.log(arreglo_bidimensional[1][0]); // A2
console.log(arreglo_bidimensional[1][1]); // B2
console.log(arreglo_bidimensional[1][2]); // C2
console.log(arreglo_bidimensional[2][0]); // A3
console.log(arreglo_bidimensional[2][1]); // B3
console.log(arreglo_bidimensional[2][2]); // C3
