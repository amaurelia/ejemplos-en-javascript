// acceder a una letra de un texto
// en JavaScript, se puede acceder a una letra de un texto utilizando la sintaxis de corchetes "[]"
// el índice de la letra comienza en 0, es decir, la primera letra tiene el índice 0, la segunda letra tiene el índice 1, y así sucesivamente   

texto = "Javascript";
console.log(texto[0]); // J
console.log(texto[1]); // a
console.log(texto[2]); // v
console.log(texto[3]); // a
console.log(texto[4]); // s
console.log(texto[5]); // c
console.log(texto[6]); // r
console.log(texto[7]); // i
console.log(texto[8]); // p
console.log(texto[9]); // t    