// funciones con Math
// en JavaScript, el objeto "Math" proporciona una serie de funciones matemáticas que se pueden utilizar para realizar cálculos y operaciones matemáticas

a = 3.12312112
b = -8

// funciones de Math
Math.abs(a) // devuelve el valor absoluto de a, en este caso 3.12312112
Math.abs(b) // devuelve el valor absoluto de b, en este caso 8  

Math.sqrt(a) // devuelve la raíz cuadrada de a, en este caso 1.767
Math.sqrt(b) // devuelve NaN, ya que no se puede calcular la raíz cuadrada de un número negativo

Math.pow(a, 2) // devuelve a elevado a la potencia de 2, en este caso 9.750
Math.pow(b, 3) // devuelve b elevado a la potencia de 3, en este caso -512
