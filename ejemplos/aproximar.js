// aproximar números
// en JavaScript, se pueden aproximar números utilizando los métodos "Math.round()", "Math.ceil()" y "Math.floor()"

a = 3.12312112
b = 3.94534534

Math.round(a) // redondea el número a su entero más cercano, en este caso 3
Math.round(b) // redondea el número a su entero más cercano, en este caso 4 

Math.ceil(a) // redondea el número hacia arriba, en este caso 4
Math.ceil(b) // redondea el número hacia arriba, en este caso 4

Math.floor(a) // redondea el número hacia abajo, en este caso 3
Math.floor(b) // redondea el número hacia abajo, en este caso 3