// mayor de edad
// en JavaScript, se puede utilizar el operador de comparación "mayor o igual que" (>=) para comparar la edad con 18
// si la edad es mayor o igual a 18, se considera que la persona es mayor de edad, de lo contrario, es menor de edad

edad = 20;
if(edad >= 18) {
    console.log("Eres mayor de edad");
}
else {
    console.log("Eres menor de edad");
}   