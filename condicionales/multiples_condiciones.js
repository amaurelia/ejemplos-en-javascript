// Ejemplo de múltiples condiciones en una declaración if
// se pueden evaluar múltiples condiciones utilizando operadores lógicos como && (AND) y || (OR)
a = 5
b = 10
c = 15

if(a < b && b < c) {
    console.log("a es menor que b y b es menor que c");
}
else {
    console.log("No se cumple la condición");
}