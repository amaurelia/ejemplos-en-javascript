// ciclo for con break
// la instrucción "break" se utiliza para salir de un ciclo antes de que termine su ejecución normal

for( let i = 0; i < 10; i++) {
    console.log("i:" + i);
    if(i == 5) {
        // la instrucción "break" se utiliza para salir de un ciclo antes de que termine su ejecución normal
        break;
    }
}